/*
Число 10 + Число 10 = 20 
Число 20 + Строка "10", приведенная к числу = 30 
*/
console.log(10 + 10 + +"10");

/*
Деление на -0
*/
console.log(10 / -"");

/*
Превращение в число не произойдет, т.к. вместо точки стоит запятая.
*/
