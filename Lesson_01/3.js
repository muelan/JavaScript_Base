"use strict";


/*
10 + 10 = 20 (Все числа)
20 + "10" = 2010 (Число + строка)
*/
console.log(10 + 10 + "10");


/*
Число 10 + Строка "10" + Число 10... 
Все превращается в строку 101010
*/
